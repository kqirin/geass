  CONSTRAINT uq_private_room_owner UNIQUE (guild_id, owner_id),
  CONSTRAINT uq_private_room_channel UNIQUE (guild_id, voice_channel_id),
  CONSTRAINT ck_private_room_whitelist_json CHECK (public.pg_p0_json_valid(whitelist_member_ids_json))
